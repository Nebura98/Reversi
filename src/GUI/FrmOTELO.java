    public void registro() {
        while (this.estado) {
            try {
                this.player1 = lblJugador1.getText();
                this.player2 = lblJugador2.getText();
                this.colorficha1 = lblColor1.getText();
                this.colorficha2 = lblColor2.getText();
                if (this.player1 != "Jugador 1" && this.player2 != "Jugador 2"
                        && (this.colorficha1 == "Negro" && (this.colorficha2 == "Blanco")
                        || (this.colorficha1 == "Blanco" && this.colorficha2 == "Negro"))) {
                    jugador1 = new Jugador(this.player1, this.colorficha1, lblPuntos1);
                    jugador2 = new Jugador(this.player2, this.colorficha2, lblPuntos2);
                    Otello.añadir(jugador1, jugador2);
                    this.jMenuItem2.setEnabled(false);
                    try {
                        time.start();
                    } catch (Exception e) {
                    }
                    this.estado = false;
                }
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
            }
        }
    }

