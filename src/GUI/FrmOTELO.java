    private void refrescar() {
        while (true) {
            for (int f = 0; f < 12; f++) {
                for (int c = 0; c < 12; c++) {
                    if (Otello.getTab().getTablero()[f][c].isCambio()) {
                        Otello.getTab().getTablero()[f][c].getBtn().setEnabled(false);
                        Otello.getTab().getTablero()[f][c].getBtn().setBounds(f * 55, c * 55, 55, 55);
                        actualizar(f, c);
                    }
                }
            }
        }
    }

    private void actualizar(int f, int c) {
        for (int k = 0; k < 23; k++) {
            Otello.getTab().getTablero()[f][c].getBtn().setDisabledIcon(new javax.swing.ImageIcon(getClass().getResource("/icono/" + Otello.getTab().getTablero()[f][c].getColor() + k + ".png")));
            try {
                Thread.sleep(15);
            } catch (InterruptedException ex) {
                Logger.getLogger(FrmOTELO.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        Otello.getTab().getTablero()[f][c].setCambio(false);
    }

